    private void carregar_pedidos() {
        FirebaseFirestore.getInstance()
                .collection("estabelecimentos")
                .document("restaurantes")
                .collection("restaurante1")//aqui vai o id do restaurante que sera recuperado na hora do login
                .document("pedidos")
                .collection("novos")
                .addSnapshotListener(new EventListener<QuerySnapshot>() {
                    @Override
                    public void onEvent(QuerySnapshot documentSnapshots, FirebaseFirestoreException e) {
                        List<DocumentSnapshot> snapshotList = documentSnapshots.getDocuments();
                        for (DocumentChange dc : documentSnapshots.getDocumentChanges()) {
                            if (dc.getType() == DocumentChange.Type.ADDED) {
                                Log.d(TAG, "Data: " + dc.getDocument().getData());
                                list.add(dc.getDocument().toObject(rd.com.vendedor.adm.item.Pedidos.class));
                                adapter.notifyItemInserted(list.size() - 1);
                            }
                        }
                    }
                });
    }

