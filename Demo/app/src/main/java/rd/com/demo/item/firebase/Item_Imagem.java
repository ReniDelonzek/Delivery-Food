package rd.com.demo.item.firebase;

public class Item_Imagem {
    private int drawable;
    public String url;
    public Item_Imagem(){}
    public Item_Imagem(String url){
        this.drawable = drawable;
        this.url = url;
    }
    public int getDrawable() {
        return drawable;
    }
    public String getUrl() {
        return url;
    }
}
